<template>
  <v-container fluid>
    <v-tabs
      v-model="page_selected"
      centered
      icons-and-text
      show-arrows
    >
      <v-tabs-slider />
      <v-tab
        v-for="page in pages"
        :key="page.value"
      >
        {{ page.title }}
        <v-icon>{{ page.icon }}</v-icon>
      </v-tab>
    </v-tabs>
    <v-tabs-items v-model="page_selected">
      <v-tab-item
        v-for="page in pages"
        :key="page.value"
      >
        <pwm-setup v-if="page.value === 'overview'" />
        <setup-overview v-else-if="page.value === 'pwm_outputs'" />
      </v-tab-item>
    </v-tabs-items>
  </v-container>
</template>

<script lang="ts">
import Vue from 'vue'

import { fetchFirmwareVehicleType, fetchVehicleType } from '@/components/autopilot/AutopilotManagerUpdater'
import PwmSetup from '@/components/vehiclesetup/PwmSetup.vue'
import setupOverview from '@/components/vehiclesetup/SetupOverview.vue'
import { callPeriodically, stopCallingPeriodically } from '@/utils/helper_functions'

export interface Item {
  title: string,
  icon: string,
  value: string,
}

export default Vue.extend({
  name: 'VehicleSetupView',
  components: {
    PwmSetup,
    setupOverview,
  },
  data() {
    return {
      page_selected: null as string | null,
      pages: [
        { title: 'Overview', icon: 'mdi-view-dashboard-variant-outline', value: 'overview' },
        { title: 'PWM Outputs', icon: 'mdi-fan', value: 'pwm_outputs' },
      ] as Item[],
    }
  },
  mounted() {
    callPeriodically(fetchVehicleType, 10000)
    callPeriodically(fetchFirmwareVehicleType, 10000)
  },
  beforeDestroy() {
    stopCallingPeriodically(fetchVehicleType)
    stopCallingPeriodically(fetchFirmwareVehicleType)
  },
})
</script>
